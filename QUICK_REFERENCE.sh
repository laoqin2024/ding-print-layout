🔧 手动命令：

   【查看进程】
   • 查看所有监听的端口
     $ lsof -i -P | grep LISTEN

   • 查看特定端口（例如 5000）
     $ lsof -i :5000

   • 查看所有 Python 进程
     $ ps aux | grep python

   • 查看所有 Node.js 进程
     $ ps aux | grep node

   ─────────────────────────────────────────────────────────────

   【停止进程 - 方式 1：按端口】
   
   • 停止 Flask（5000 端口）- 一行命令
     $ lsof -i :5000 | grep LISTEN | awk '{print $2}' | xargs kill -9

   • 停止 Vite（5173 端口）- 一行命令
     $ lsof -i :5173 | grep LISTEN | awk '{print $2}' | xargs kill -9

   • 停止两个端口
     $ lsof -i :5000,:5173 | grep LISTEN | awk '{print $2}' | xargs kill -9

   ─────────────────────────────────────────────────────────────

   【停止进程 - 方式 2：按进程名称】

   • 停止所有 Python 进程
     $ pkill -9 -f python

   • 停止所有 Flask 服务
     $ pkill -9 -f "python.*run.py"

   • 停止所有 Vite 服务
     $ pkill -9 -f vite

   • 停止所有 Node.js 进程
     $ killall node

   ─────────────────────────────────────────────────────────────

   【停止进程 - 方式 3：按 PID】

   # 获取 PID
   $ lsof -i :5000 | awk 'NR==2 {print $2}'

   # 温和停止（允许进程清理资源）
   $ kill <PID>

   # 强制停止（立即杀死）
   $ kill -9 <PID>

